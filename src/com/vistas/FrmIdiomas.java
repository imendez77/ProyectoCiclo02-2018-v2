 public void clean()
    {
        this.txtIdIdiomas.setText("");
        this.cbxIdiomas.setEnabled(false);
        this.cbxNivel.setEnabled(false);
        this.txtLugarEstudio.setEnabled(false);
    }
    
    public void bottons(int opc)
    {
        if(opc==1)
        {
            this.btnDelete.setEnabled(false);
            this.btnInsert.setEnabled(false);
            this.btnModify.setEnabled(false);
            this.btnNew.setEnabled(true);
            this.btnCancel.setEnabled(false);
            
            this.txtIdIdiomas.setEnabled(false);
            this.cbxIdiomas.setEnabled(false);
            this.cbxNivel.setEnabled(false);
            this.txtLugarEstudio.setEnabled(false);
        }else if(opc==2){
            this.btnDelete.setEnabled(true);
            this.btnInsert.setEnabled(false);
            this.btnModify.setEnabled(true);
            this.btnNew.setEnabled(false);
            this.btnCancel.setEnabled(true);
            
            this.txtIdIdiomas.setEnabled(false);
            this.cbxIdiomas.setEnabled(true);
            this.cbxNivel.setEnabled(true);
            this.txtLugarEstudio.setEnabled(true);
            
        }else if(opc==3){
            this.btnDelete.setEnabled(false);
            this.btnInsert.setEnabled(true);
            this.btnModify.setEnabled(false);
            this.btnNew.setEnabled(false);
            this.btnCancel.setEnabled(true);
            
            this.txtIdIdiomas.setEnabled(false);
            this.cbxIdiomas.setEnabled(true);
            this.cbxNivel.setEnabled(true);
            this.txtLugarEstudio.setEnabled(true);
        }
    }
